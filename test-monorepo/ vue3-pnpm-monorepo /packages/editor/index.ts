export const add = (a, b) => {
  return a + b;
};

export const sub = (a, b) => {
  return a - b;
};
export const mul = (a, b) => {
  return a * b;
};

export const div = (a, b) => {
  return a / b;
};

export const mod = (a, b) => {
  return a % b;
};

export const pow = (a, b) => {
  return a ** b;
};

export const sqrt = (a) => {
  return Math.sqrt(a);
};
